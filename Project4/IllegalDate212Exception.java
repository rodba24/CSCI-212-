/**
 * The IllegalDate212Exception class extends IllegalArgumentException
 * and represents an exception for invalid Date212 instances.
 * It is thrown when attempting to create a Date212 object with an invalid date.
 * 
 * @author Rodba K.
 */
public class IllegalDate212Exception extends IllegalArgumentException {
    
    /**
     * Constructs an IllegalDate212Exception with the specified error message.
     * 
     * @param message The error message associated with the exception.
     */
    public IllegalDate212Exception(String message) {
        super(message);
    }
    
}
